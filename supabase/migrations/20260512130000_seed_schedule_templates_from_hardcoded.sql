INSERT INTO public.schedule_templates (branch_id, course_type_id, day_of_week, start_time, end_time, is_active, notes)
SELECT
  branches.id,
  course_types.id,
  seed.day_of_week,
  seed.start_time,
  seed.end_time,
  true,
  seed.notes
FROM seed
JOIN public.branches ON branches.slug = seed.branch_slug
JOIN public.course_types ON course_types.name = seed.course_type_name::public.course_type_name
WHERE NOT EXISTS (
  SELECT 1
  FROM public.schedule_templates existing
  WHERE existing.branch_id = branches.id
    AND existing.course_type_id = course_types.id
    AND existing.day_of_week = seed.day_of_week
    AND existing.start_time = seed.start_time
    AND existing.end_time = seed.end_time
);
